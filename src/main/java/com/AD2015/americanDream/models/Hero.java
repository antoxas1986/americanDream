package com.AD2015.americanDream.models;

public class Hero {

	private String name;
	private Integer age;
	private String gender;
	private Integer experiance;
	private Integer money;
	private Integer strengh;
	private Integer level;
	private Integer happieness;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public Integer getExperiance() {
		return experiance;
	}
	public void setExperiance(Integer experiance) {
		this.experiance = experiance;
	}
	public Integer getMoney() {
		return money;
	}
	public void setMoney(Integer money) {
		this.money = money;
	}
	public Integer getStrengh() {
		return strengh;
	}
	public void setStrengh(Integer strengh) {
		this.strengh = strengh;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getHappieness() {
		return happieness;
	}
	public void setHappieness(Integer happieness) {
		this.happieness = happieness;
	}
	
}
